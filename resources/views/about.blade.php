            </div>
            <div class="about-content-main-body">
                <p>Republic Act 9184 Otherwise Known as the Government Procurement Reform Act under Section 12 and its 2016 Implementing Rules and Regulations (IRR), requires the creation of the Bids and Awards Committee (BAC), Bids and Awards Committee Secretariat (BACSec) and Technical Working Group by the Head of
                    Procuring Entity (HoPE). The BAC has a fixed term of one (1) year reckoned from the date of appointment, renewable at the discretion of the HoPE.</p>
                <p>To facilitate and harmonize procedures and standards in the procurement process of the University, there are three (3) BACs, such as:</p>
            </div>
            <br>
            <div class="about-content-items">
                <ul>
                    <li style="list-style: none;">
                        <span class="list-style">➣ </span><b>BAC Goods/Regular.</b>
                        <span>The BAC Goods/Regular is in-charge in the procurement activities under the General Appropriations Act (GAA), Revolving and Internally Funded Programs and Projects of the University.</span>
                    </li>
                    <div class="about-divider"></div>
                    <li style="list-style: none;">
                        <span class="list-style">➣ </span><b>BAC Research.</b>
                        <span>The BAC Research is in-charge in the procurement activities under the externally funded research programs and projects.</span>
                    </li>
                    <div class="about-divider"></div>
                    <li style="list-style: none;">
                        <span class="list-style">➣ </span><b>BAC Infrastructure.</b>
                        <span>The BAC Infrastructure is in-charge in the procurement activities under the General, Revolving, Internally and Externally funded infrastructure programs and projects.</span>
                    </li>
                </ul>
                <p class="bottom-divider">
                    The above committees are composed of five (5) regular members following Section 11.2 of RA 9184 IRR:
                </p>
                <br>
                <ul>
                    <li style="list-style: none;">
                        <span class="list-style">☉</span><span class="list-italic"> Chairperson, who is at least a third (3rd) ranking permanent official of the Procuring Entity;</span>
                    </li>
                    <div class="about-divider"></div>
                    <li style="list-style: none;">
                        <span class="list-style">☉</span><span class="list-italic"> An officer, who is at least a fifth (5th) ranking permanent official, or if not available, an officer of the next lower rank with knowledge, experience and/or expertise in procurement who, to the extent possible, represents the legal or administrative area of the Procuring Entity: Provided, That in the case of bureaus, regional offices and sub-regional/district offices, BAC members shall be at least a third (3rd) ranking permanent personnel or if not available, an officer of the next lower rank;</span>
                    </li>
                </ul>
                <br>
                <p class="bottom-divider" style="font-style: italic;">
                    The above committees are composed of five (5) regular members following Section 11.2 of RA 9184 IRR:
                </p>
                <br>
                <ul>
                    <li style="list-style: none;">
                        <span class="list-style">☉</span><span class="list-italic"> An officer who has technical expertise relevant to the procurement at hand, and, to the extent possible, has knowledge, experience and/or expertise in procurement; and</span>
                    </li>
                    <div class="about-divider"></div>
                    <li style="list-style: none;">
                        <span class="list-style">☉</span><span class="list-italic"> A representative from the end-user or implementing unit who has knowledge of procurement laws and procedures. When procurement tasks are outsourced to another GoP agency as a procurement agent pursuant to Section 7.3.3, a representative from the Procuring Entity may be designated as a provisional member to the BAC. For this purpose, the procuring entity shall recommend the provisional member to be designated.</span>
                    </li>
                    <div class="about-divider"></div>
                    <li style="list-style: none;">
                        <span class="list-style">☉</span><span class="list-italic"> An officer, who is at least a fifth (5th) ranking permanent official, or if not available, an officer of the next lower rank with knowledge, experience and/or expertise in procurement who, to the extent possible, represents the finance area of the Procuring Entity: Provided, That in the case of bureaus, regional offices and sub-regional/district offices, BAC members shall be at least a third (3rd) ranking permanent personnel or if not available, an officer of the next lower rank;</span>
